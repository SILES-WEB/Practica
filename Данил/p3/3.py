# Создание списка коробок
boxes = [
    {'длина': 1, 'ширина': 1, 'высота': 1, 'вес': 1},
    {'длина': 5, 'ширина': 5, 'высота': 5, 'вес': 10},
    {'длина': 100, 'ширина': 100, 'высота': 100, 'вес': 100},
    # Добавьте другие коробки по аналогии, если нужно
]

# Распределение коробок по объему
m = []
s = []
b = []

for box in boxes:
    объем = box['длина'] * box['ширина'] * box['высота']
    if объем < 100:
        m.append(box)
    elif 100 <= объем < 500:
        s.append(box)
    else:
        b.append(box)

# Вывод результатов
print("Маленькие коробки:", m)
print("Средние коробки:", s)
print("Большие коробки:", b)

